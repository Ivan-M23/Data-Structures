package com.ivan.dataStructures;

public class StaticArray {
	
	int length;
	int intData;
	String stringData;
	
	public StaticArray(int length, String data) {
		this.length = length;
		this.stringData = data;
	}
	public StaticArray(int length, int data) {
		this.length = length;
		this.intData = data;
	}

}
